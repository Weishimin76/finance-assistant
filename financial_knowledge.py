def search_knowledge(query, threshold=0.3, max_results=10):
    """
    根据关键词智能搜索匹配的知识条目。

    支持模糊搜索和关键词匹配，综合考虑问题文本、关键词和分类的匹配度。

    Args:
        query (str): 搜索查询字符串
        threshold (float): 匹配阈值（0-1），默认0.3
        max_results (int): 最大返回结果数，默认10

    Returns:
        list: 匹配的知识条目列表，按相关度排序，每个条目包含原始数据和相关度分数

    Example:
        >>> results = search_knowledge("英国VAT税率")
        >>> for item in results:
        ...     print(f"[{item['score']:.2f}] {item['question']}")
    """
    if not query or not isinstance(query, str):
        return []

    query = query.strip().lower()

    # 将查询字符串拆分为关键词（支持中文和英文）
    # 中文按字符和常见词拆分，英文按空格拆分
    query_tokens = _tokenize_query(query)

    results = []

    for item in FINANCIAL_KNOWLEDGE_BASE:
        score = 0.0

        # 1. 关键词精确匹配（权重最高）
        for keyword in item.get("keywords", []):
            keyword_lower = keyword.lower()
            for token in query_tokens:
                if token in keyword_lower or keyword_lower in token:
                    score += 0.3
                # 完全匹配加分
                if token == keyword_lower:
                    score += 0.2

        # 2. 问题文本模糊匹配
        question_lower = item.get("question", "").lower()
        for token in query_tokens:
            if token in question_lower:
                score += 0.15
            # 使用序列相似度进行模糊匹配
            similarity = SequenceMatcher(None, token, question_lower).ratio()
            if similarity > 0.5:
                score += similarity * 0.1

        # 3. 分类匹配
        category_lower = item.get("category", "").lower()
        for token in query_tokens:
            if token in category_lower or category_lower in token:
                score += 0.1

        # 4. 整体查询与问题的相似度
        overall_similarity = SequenceMatcher(None, query, question_lower).ratio()
        if overall_similarity > 0.3:
            score += overall_similarity * 0.2

        # 5. 整体查询与答案的相似度（权重较低）
        answer_lower = item.get("answer", "").lower()
        for token in query_tokens:
            if token in answer_lower:
                score += 0.05

        if score >= threshold:
            result = dict(item)
            result["score"] = round(score, 4)
            results.append(result)

    # 按相关度分数降序排序
    results.sort(key=lambda x: x["score"], reverse=True)

    return results[:max_results]


def _tokenize_query(query):
    """
    将查询字符串拆分为搜索关键词。

    支持中文字符、英文单词和数字的拆分。

    Args:
        query (str): 查询字符串

    Returns:
        list: 关键词列表
    """
    tokens = []

    # 按空格拆分
    space_parts = query.split()

    for part in space_parts:
        # 分离中文字符和英文/数字
        segments = re.findall(r'[\u4e00-\u9fff]+|[a-zA-Z0-9]+', part)
        if segments:
            tokens.extend(segments)
        else:
            tokens.append(part)

    # 对中文部分尝试提取2-3字的词组
    expanded_tokens = list(tokens)
    for token in tokens:
        if re.match(r'^[\u4e00-\u9fff]+$', token) and len(token) >= 2:
            # 提取2字子串
            for i in range(len(token) - 1):
                bigram = token[i:i + 2]
                if bigram not in expanded_tokens:
                    expanded_tokens.append(bigram)
            # 提取3字子串
            for i in range(len(token) - 2):
                trigram = token[i:i + 3]
                if trigram not in expanded_tokens:
                    expanded_tokens.append(trigram)

    return expanded_tokens


def get_by_category(category):
    """
    按分类获取知识条目。

    Args:
        category (str): 分类名称，如"跨境税务"、"外贸财务"等。
                        支持模糊匹配，会查找包含该字符串的分类。

    Returns:
        list: 该分类下的所有知识条目列表。如果未找到匹配分类，返回空列表。

    Example:
        >>> items = get_by_category("跨境税务")
        >>> for item in items:
        ...     print(item["question"])
    """
    if not category or not isinstance(category, str):
        return []

    category = category.strip()

    # 精确匹配
    exact_matches = [
        item for item in FINANCIAL_KNOWLEDGE_BASE
        if item.get("category") == category
    ]

    if exact_matches:
        return exact_matches

    # 模糊匹配：查找包含查询字符串的分类
    fuzzy_matches = [
        item for item in FINANCIAL_KNOWLEDGE_BASE
        if category.lower() in item.get("category", "").lower()
    ]

    return fuzzy_matches


def get_all_categories():
    """
    获取所有分类列表。

    Returns:
        list: 所有分类名称的列表，按首次出现的顺序排列，无重复。

    Example:
        >>> categories = get_all_categories()
        >>> for cat in categories:
        ...     print(cat)
    """
    seen = set()
    categories = []

    for item in FINANCIAL_KNOWLEDGE_BASE:
        cat = item.get("category")
        if cat and cat not in seen:
            seen.add(cat)
            categories.append(cat)

    return categories


